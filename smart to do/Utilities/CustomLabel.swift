import Foundation
import UIKit

public class Inter400Size20Color061053: UILabel {
    let fontSize: CGFloat = FontSize.size20.rawValue

    public override func awakeFromNib() {
        super.awakeFromNib()

        setLabel()
    }

    public override init(frame: CGRect) {
        super.init(frame: frame)

        setLabel()
    }

    required init?(coder: NSCoder) {
        super.init(coder: coder)
    }

    private func setLabel() {
        self.textColor = CustomColors().color061053
        self.font = UIFont(name: Fonts.Inter.weight400, size: getFontSizeWithRespectToDevice(minimumFontSize: fontSize))
    }
}

